import argparse

import mss
import numpy as np
from utils.datasets import *
from utils.utils import *


def detect(save_img=False):
    out, source, weights, view_img, save_txt, imgsz = \
        opt.output, opt.source, opt.weights, opt.view_img, opt.save_txt, opt.img_size

    # Initialize
    device = torch_utils.select_device(opt.device)
    if os.path.exists(out):
        shutil.rmtree(out)  # delete output folder
    os.makedirs(out)  # make new output folder
    half = device.type != 'cpu'  # half precision only supported on CUDA

    # Load model
    model = torch.load(weights, map_location=device)['model'].float()  # load to FP32
    model.to(device).eval()
    if half:
        model.half()  # to FP16

    # Set Dataloader
    save_img = True

    # Run inference
    img = torch.zeros((1, 3, imgsz, imgsz), device=device)  # init img
    _ = model(img.half() if half else img) if device.type != 'cpu' else None  # run once

    path = "inference\\images\\bus.jpg"
    # Part of the screen to capture
    monitor = {"top": 40, "left": 0, "width": 800, "height": 640}
    with mss.mss() as sct:
        sc = sct.grab(monitor) # BGRA
        im0s = np.array(sc)[:, :, :3]
        im0s = cv2.cvtColor(im0s, cv2.COLOR_BGRA2BGR)

        cv2.rectangle(im0s, (10, 10), (50, 50), (255, 0, 0), thickness=3, lineType=cv2.LINE_AA)

        img = letterbox(im0s, new_shape=imgsz)[0]  # resize image (keep ratio)
        img = img[:, :, ::-1].transpose(2, 0, 1)  # BGR to RGB, to 3x416x416
        img = np.ascontiguousarray(img)

        img = torch.from_numpy(img).to(device)
        img = img.half() if half else img.float()  # uint8 to fp16/32
        img /= 255.0  # 0 - 255 to 0.0 - 1.0
        if img.ndimension() == 3:
            img = img.unsqueeze(0)

        # Inference
        pred = model(img, augment=opt.augment)[0]

        # Apply NMS
        pred = non_max_suppression(pred, opt.conf_thres, opt.iou_thres, classes=opt.classes, agnostic=opt.agnostic_nms)

        # Process detections
        for i, det in enumerate(pred):  # detections per image
            p, s, im0 = path, '', im0s

            save_path = str(Path(out) / Path(p).name)
            if det is not None and len(det):
                # Rescale boxes from img_size to im0 size
                det[:, :4] = scale_coords(img.shape[2:], det[:, :4], im0.shape).round()

                # Write results
                for *xyxy, conf, cls in det:
                    label = '%s %.2f' % (int(cls), conf)
                    # TODO - move function into there
                    plot_one_box(xyxy, im0, label=label, color=int(cls), line_thickness=3)

            # Save results (image with detections)
            cv2.imwrite(save_path, im0)
            cv2.imshow('My Image', im0)
            cv2.waitKey(0)
            cv2.destroyAllWindows()

    print('Done')


